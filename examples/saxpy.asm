; saxpy.asm: SAXPY: Y[i] = a*X[i] + Y[i] for i = 0..3
;
; a = 2.0
; X = [1.0, 2.0, 3.0, 4.0]  stored at words 50-53
; Y = [10.0, 20.0, 30.0, 40.0]  stored at words 54-57
;
; Result: Y = [12.0, 24.0, 36.0, 48.0]
;
; Cray-1 FP: bit63=sign, bits62:48=exp (bias 16384), bits47:0=coeff (bit47=1 normalized).
; 12.0 = 0.75*2^4: exp=0x4004, coeff=0xC00000000000
; 24.0 = 0.75*2^5: exp=0x4005, coeff=0xC00000000000
; 36.0 = 0.5625*2^6: exp=0x4006, coeff=0x900000000000
; 48.0 = 0.75*2^6: exp=0x4006, coeff=0xC00000000000
;
; Expect: S1=0x4004C00000000000 S2=0x4005C00000000000 S3=0x4006900000000000 S4=0x4006C00000000000

    ; S7 = 2^47 = 0x0000_8000_0000_0000 (coefficient MSB for .5 fraction, reused below)
    si 7, 1
    sshl 7, 47

    ; -----------------------------------------------------------------------
    ; S0 = a = 2.0 = 0x4002_8000_0000_0000
    ; -----------------------------------------------------------------------
    si 0, 16386
    sshl 0, 48
    sadd 0, 0, 7

    ; -----------------------------------------------------------------------
    ; Build X array and store to words 50-53 (A0=0, absolute offset in stores)
    ; -----------------------------------------------------------------------

    ; X[0] = 1.0 = 0x4001_8000_0000_0000
    si 1, 16385
    sshl 1, 48
    sadd 1, 1, 7
    stores 1, 0, 50

    ; X[1] = 2.0 — same encoding as a, already in S0
    stores 0, 0, 51

    ; X[2] = 3.0 = 0x4002_C000_0000_0000  (coeff = 3 << 46)
    si 1, 16386
    sshl 1, 48
    si 2, 3
    sshl 2, 46
    sadd 1, 1, 2
    stores 1, 0, 52

    ; X[3] = 4.0 = 0x4003_8000_0000_0000
    si 1, 16387
    sshl 1, 48
    sadd 1, 1, 7
    stores 1, 0, 53

    ; -----------------------------------------------------------------------
    ; Build Y array and store to words 54-57
    ; -----------------------------------------------------------------------

    ; Y[0] = 10.0 = 0x4004_A000_0000_0000  (coeff = 5 << 45)
    si 1, 16388
    sshl 1, 48
    si 2, 5
    sshl 2, 45
    sadd 1, 1, 2
    stores 1, 0, 54

    ; Y[1] = 20.0 = 0x4005_A000_0000_0000
    si 1, 16389
    sshl 1, 48
    si 2, 5
    sshl 2, 45
    sadd 1, 1, 2
    stores 1, 0, 55

    ; Y[2] = 30.0 = 0x4005_F000_0000_0000  (coeff = 15 << 44)
    si 1, 16389
    sshl 1, 48
    si 2, 15
    sshl 2, 44
    sadd 1, 1, 2
    stores 1, 0, 56

    ; Y[3] = 40.0 = 0x4006_A000_0000_0000
    si 1, 16390
    sshl 1, 48
    si 2, 5
    sshl 2, 45
    sadd 1, 1, 2
    stores 1, 0, 57

    ; -----------------------------------------------------------------------
    ; Vector setup: VL = 4
    ; -----------------------------------------------------------------------
    ai 1, 4
    setvl 1

    ; -----------------------------------------------------------------------
    ; Load X into V1, Y into V2
    ; -----------------------------------------------------------------------
    ai 0, 50
    vload 1, 0       ; V1[0..3] = X[0..3]
    ai 0, 54
    vload 2, 0       ; V2[0..3] = Y[0..3]

    ; -----------------------------------------------------------------------
    ; SAXPY core
    ; -----------------------------------------------------------------------
    vfmul 1, 0, 1    ; V1[i] = S0 * V1[i]  = a * X[i]
    vfaddv 2, 1, 2   ; V2[i] = V1[i] + V2[i] = a*X[i] + Y[i]

    ; Write results back to Y array
    ai 0, 54
    vstore 2, 0

    ; -----------------------------------------------------------------------
    ; Extract V2[0..3] into S1-S4 for test verification
    ; -----------------------------------------------------------------------
    ai 0, 0
    vget 1, 2, 0     ; S1 = V2[0] = 12.0
    ai 0, 1
    vget 2, 2, 0     ; S2 = V2[1] = 24.0
    ai 0, 2
    vget 3, 2, 0     ; S3 = V2[2] = 36.0
    ai 0, 3
    vget 4, 2, 0     ; S4 = V2[3] = 48.0

    exit
